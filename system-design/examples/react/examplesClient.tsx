const examples = {} as Record<string, any>;

function defineExample(key: string, fn: any, useCase: string) {
  examples[key] = fn;
  examples[key].useCase = useCase;
}

for (const register of registers) {
  register(defineExample);
}

export const exampleKeys = Object.freeze(Object.keys(examples));

export function runExample(key: string, ...args: any[]) {
  const entry = examples[key];
  if (!entry) {
    throw new Error(`Example "${key}" not found.`);
  }
  return entry(...args);
}

export function getUseCase(key: string) {
  return examples[key]?.useCase ?? '';
}

export { examples };
